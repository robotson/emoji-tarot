class OGImageRewriter {
  constructor(emoji_slugs) {
    this.emoji_slugs = emoji_slugs;
  }
  element(element) {
    console.log("in element rewriter");
    const new_image = `https://preview.emoji-tarot.com/${this.emoji_slugs.join(
      "_"
    )}.png`;
    element.setAttribute("content", new_image);
  }
}

class OGUrlRewriter {
  constructor(emoji_slugs) {
    this.emoji_slugs = emoji_slugs;
  }
  element(element) {
    const new_url = `https://emoji-tarot.com/cards/${this.emoji_slugs.join(
      "/"
    )}`;
    element.setAttribute("content", new_url);
  }
}

